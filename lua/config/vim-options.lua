--------- OPTIONS (separated from keybinds) -----------
-- Set line-numbers
vim.cmd("set number")
vim.cmd('set relativenumber')

-- Set tab = 2 spaces
vim.cmd("set expandtab")
vim.cmd("set tabstop=2")
vim.cmd("set softtabstop=2")
vim.cmd("set shiftwidth=2")

-- Style floating windows
vim.cmd("set winborder=rounded")
